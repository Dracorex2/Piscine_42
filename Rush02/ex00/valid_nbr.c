int	ft_valid_nbr(char *str)
{
	int	i;
	int	sign;

	i = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	if (sign == -1)
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
		i++;
	while (str[i++])
		str[i] = '\0';
}